 	<h3>Input library for sanitizing</h3>
	<p>The Input library from <a href="http://codeigniter.com" target="_blank">CodeIgniter</a> has been integrated to provide an easy and secure way to filter $_GET, $_POST, $_COOKIE and $_SERVER variables.</p>
	<p>
		<strong>Examples:</strong> From controller, model or view, do<br />
		<em>$this->input->get('var');</em><br />
		<em>$this->input->post('var');</em><br />
		<em>$this->input->cookie('var');</em><br />
		<em>$this->input->server('var');</em><br />
	</p>
