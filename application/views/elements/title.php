<h1>MVCLight demo application</h1>
<p>
	MVCLight is a lightweight MVC framework written in PHP, suitable for custom projects where a home-brew MVC base is needed, and for educational purposes. It is based on the <a href="http://www.phpro.org/tutorials/Model-View-Controller-MVC.html" target="_blank">MVC tutorial</a> by <a href="http://www.phpro.org" target="_blank">PHPRO.org</a>.
</p>